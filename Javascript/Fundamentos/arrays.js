//JS aceita todos os tipos de valores dentro de um mesmo array, sendo possível mistura-los

const valores = [1, 5.2, 7.1, 8.3]
console.log(valores[0],valores [3]) //Imprimindo a primeira posição e a terceira

console.log(valores.length) //Quantidade de posições dentro do array

valores.push(3, 7) //Adiciona valores ao final do array
console.log(valores)

valores.pop() //Deleta a última posição do array
console.log(valores)
